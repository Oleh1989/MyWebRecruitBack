CREATE TABLE [dbo].[Job_General] (
    [JOB_ID]        INT            NOT NULL,
    [NAME]          NVARCHAR (100) NOT NULL,
    [ADDRESS_LINE]  NVARCHAR (100) NULL,
    [ADDRESS_CITY]  NVARCHAR (100) NULL,
    [ADDRESS_INDEX] NVARCHAR (10)  NULL,
    [COUNTRY]       INT            NULL,
    [JOB_CURRENCY] NCHAR(10) NULL, 
    [JOB_STATUS] NVARCHAR(50) NULL, 
    [SATRT_DT] DATE NULL, 
    [END_DT] DATE NULL, 
    [NO_REQ] SMALLINT NOT NULL, 
    [CATEGORY] NVARCHAR(50) NOT NULL, 
    [SALARY] MONEY NULL, 
    [INTERSHIP_YN] TINYINT NULL, 
    [PAY_METHOD] INT NULL, 
    CONSTRAINT [PK_Job_General] PRIMARY KEY CLUSTERED ([JOB_ID] ASC),
    CONSTRAINT [FK_Job_General_Country] FOREIGN KEY ([COUNTRY]) REFERENCES [dbo].[Country] ([COUNTRY_ID]),
	CONSTRAINT [FK_Job_General_Pay_Method] FOREIGN KEY ([PAY_METHOD]) REFERENCES [dbo].[Pay_Method] ([PAY_METHOD_ID])
);
